    private void buttonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalcularActionPerformed

        Double valor = Double.parseDouble(textValor.getText().replaceAll(",", ""));

        OpcaoImpostos opcao = (OpcaoImpostos) comboBoxImposto.getSelectedItem();

        CalculaImpostos calculador = new CalculaImpostos(valor, opcao.getImposto());
        Double resultado = calculador.execute();

        DecimalFormat decimal = new DecimalFormat("R$ ###,###.##");

        labelResultado.setText(decimal.format(resultado));
        labelPorcentagem.setText(calculador.porcentagem().toString());
        
        labelResultado.setVisible(true);
        labelPorcentagem.setVisible(true);
    }//GEN-LAST:event_buttonCalcularActionPerformed

